a = input("Digite qualquer coisa:\n")
print('O que você digitou é do tipo:',type(a))
print('Só tem espaços?', a.isspace())
print('É um número?', a.isnumeric())
print('É alfabético?', a.isalpha())
print('Só tem maiúsculas?', a.isupper())
print('Só tem minúsculas?', a.islower())
print('É alfanumérico?', a.isalnum())
print('Está capitalizada?', a.istitle())

# O a é chamado de objeto. Cada objeto tem características e realiza funcionalidades.

# Existem atributos e existem métodos.
# Todos os .is() são considerados métodos. Os métodos são as ações que um objeto pode realizar.
# Atributos são as características de um objeto, essas características também são conhecidas como variáveis.
